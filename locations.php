 <h2>Galle Fort</h2>
    <span><p>If you have not visited the Galle Fort, you have not truly experienced the city of Galle! The 17 th Century structural masterpiece of a bygone era is considered to be one of the finest preserved pieces of architecture of the Old-World in the whole of Asia. Within its fortifications lie some fascinating buildings that tell an intriguing tale of the colonial days. Today, the fort is an eclectic mix of history, culture, food and recreation all rolled into one.</p></span>
