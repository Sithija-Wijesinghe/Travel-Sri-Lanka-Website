 <h2>unawatuna</h2>
    <span><p>Unawatuna is home to a number of beautiful golden beaches, making it one of the most visited places on the southern coast. Located about 6km away from the city of Galle, the Unawatuna bay is extremely popular amongst both locals and tourists. The natural coral reef makes it safe to swim and engage in water sports such as surfing, jet skiing, snorkelling and diving. The beach is lined with guest houses, bars and restaurants, making the stretch one of the most active after sundown. Most of the restaurants in Unawatuna are famous for hosting epic parties that attract people from all over the island. The area is certainly the best place for night-life in Galle.</p></span>
