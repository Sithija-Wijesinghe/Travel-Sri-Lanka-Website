 <h2>Rumassala</h2>
    <span><p>Adventure and myth await those who venture towards the Rumassala Hill located in Unawatuna. The hill is located just over 5km away from the Galle Fort, and is one of the best places in the vicinity to embark on a hike. Believed to be a chunk of the Himalaya mountains, dropped by the flying monkey-god Hanuman, this hill has a very unique ecosystem which is unlike any other in the region. Another theory is that the hill is actually a meteorite that crashed in Unawatuna. Rumassala is a lush environment, and is home to many endemic species of flora and fauna which can be viewed during your hike.</p></span>
